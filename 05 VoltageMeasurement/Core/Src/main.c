/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	/* USER CODE BEGIN 1 */
	//variables to store data from ADC and converted into voltage data
	float mcuVoltage = 0;
	float batteryVoltage = 0;
	uint16_t adcData[2];
	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_DMA_Init();
	MX_I2C1_Init();
	MX_ADC1_Init();
	/* USER CODE BEGIN 2 */
	ssd1306_Init();
	//start ADC DMA conversion
	HAL_ADCEx_Calibration_Start(&hadc1);
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*) adcData, 2);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		mcuVoltage = ADC_MAX * ADC_REFERENCE_VOLTAGE / adcData[0];
		batteryVoltage = 2 * adcData[1] * mcuVoltage / ADC_MAX;
		char buf[16] =
		{ 0 };
		sprintf(buf, "%f", mcuVoltage);
		ssd1306_Fill(Black);
		ssd1306_SetCursor(0, 0);
		ssd1306_WriteString("MCU V =", Font_7x10, White);
		ssd1306_WriteString(buf, Font_7x10, White);
		sprintf(buf, "%f", batteryVoltage);
		ssd1306_SetCursor(0, 30);
		ssd1306_WriteString("Voltage =", Font_7x10, White);
		ssd1306_WriteString(buf, Font_7x10, White);
		ssd1306_UpdateScreen();
		HAL_Delay(250);  // wait for a while
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

